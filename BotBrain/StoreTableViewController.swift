    func asyncHandler(r: HTTPResult) {
        if let dictionary = r.json! as? [String] {
            self.noOfProducts = dictionary.count
            self.productList = dictionary
        }
        DispatchQueue.main.async {
            self.tableView.reloadData()
        }
        LoaderController.sharedInstance.removeLoader()
    }
    // MARK: - Table view data source

